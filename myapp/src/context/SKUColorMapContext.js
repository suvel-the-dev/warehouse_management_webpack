const getSkuDescColorMap = () => {
    let skuDescColorMap = {}
    skuDescList.forEach(skuDesc => {
        skuDescColorMap[skuDesc] = randomColor({
            luminosity: 'dark',
            alpha: 5
        })
    })
    return skuDescColorMap
}

export const SKUColorMapProvider = ({ children }) => {
    const [changeColor, setChangeColor] = useState(true);

    const skuDescColorMap = useMemo(() => getSkuDescColorMap(), [changeColor]);

    const value = { setChangeColor, skuDescColorMap }
    return (
        <SKUColorMapContext.Provider value={value}>
            {children}
        </SKUColorMapContext.Provider>
    )
}
